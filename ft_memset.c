#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*p;
	size_t			i;

	p = s;
	i = 0;
	while (n > i)
	{
		p[i] = c;
		i++;
	}
	return (s);
}
